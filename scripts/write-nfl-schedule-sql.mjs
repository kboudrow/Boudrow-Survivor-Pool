import { writeFile } from 'node:fs/promises'

const season = Number(process.argv[2] || 2026)
const outFile = process.argv[3] || `supabase/nfl-schedule-${season}.sql`

const ESPN_TO_APP_TEAM = {
  ARI: 'ARI',
  ATL: 'ATL',
  BAL: 'BAL',
  BUF: 'BUF',
  CAR: 'CAR',
  CHI: 'CHI',
  CIN: 'CIN',
  CLE: 'CLE',
  DAL: 'DAL',
  DEN: 'DEN',
  DET: 'DET',
  GB: 'GB',
  HOU: 'HOU',
  IND: 'IND',
  JAX: 'JAX',
  KC: 'KC',
  LV: 'LV',
  LAC: 'LAC',
  LAR: 'LAR',
  MIA: 'MIA',
  MIN: 'MIN',
  NE: 'NE',
  NO: 'NO',
  NYG: 'NYG',
  NYJ: 'NYJ',
  PHI: 'PHI',
  PIT: 'PIT',
  SEA: 'SEA',
  SF: 'SF',
  TB: 'TB',
  TEN: 'TEN',
  WSH: 'WAS',
  WAS: 'WAS',
}

function sqlString(value) {
  return `'${String(value).replaceAll("'", "''")}'`
}

function appTeam(espnAbbr) {
  const mapped = ESPN_TO_APP_TEAM[espnAbbr]
  if (!mapped) throw new Error(`No team mapping for ESPN abbreviation: ${espnAbbr}`)
  return mapped
}

function getWeekSundayDate(events) {
  const sunday = events
    .map((event) => new Date(event.date))
    .find((date) => date.getUTCDay() === 0)

  if (!sunday) {
    throw new Error(`Could not find a Sunday game for week ${events[0]?.week?.number}`)
  }

  return sunday.toISOString().slice(0, 10)
}

async function fetchWeek(week) {
  const url = new URL('https://site.api.espn.com/apis/site/v2/sports/football/nfl/scoreboard')
  url.searchParams.set('dates', String(season))
  url.searchParams.set('seasontype', '2')
  url.searchParams.set('week', String(week))

  const response = await fetch(url)
  if (!response.ok) {
    throw new Error(`ESPN request failed for week ${week}: ${response.status} ${response.statusText}`)
  }

  const body = await response.json()
  return body.events || []
}

const games = []
const seasonWeeks = []

for (let week = 1; week <= 18; week += 1) {
  const events = await fetchWeek(week)
  if (events.length === 0) {
    throw new Error(`No games returned for week ${week}`)
  }

  seasonWeeks.push({
    season,
    week,
    weekSundayDate: getWeekSundayDate(events),
  })

  for (const event of events) {
    const competition = event.competitions?.[0]
    const home = competition?.competitors?.find((team) => team.homeAway === 'home')
    const away = competition?.competitors?.find((team) => team.homeAway === 'away')

    if (!competition || !home || !away) {
      throw new Error(`Missing competition/home/away data for ESPN event ${event.id}`)
    }

    games.push({
      season,
      week,
      gameTime: competition.startDate || competition.date || event.date,
      homeTeam: appTeam(home.team.abbreviation),
      awayTeam: appTeam(away.team.abbreviation),
      espnEventId: String(event.id),
    })
  }
}

const gameValues = games
  .map((game) =>
    [
      game.season,
      game.week,
      sqlString(game.gameTime),
      sqlString(game.gameTime),
      sqlString(game.homeTeam),
      sqlString(game.awayTeam),
      sqlString('scheduled'),
      sqlString(game.espnEventId),
    ].join(', ')
  )
  .map((row) => `  (${row})`)
  .join(',\n')

const seasonWeekValues = seasonWeeks
  .map((week) => `  (${week.season}, ${week.week}, ${sqlString(week.weekSundayDate)})`)
  .join(',\n')

const sql = `-- Generated by scripts/write-nfl-schedule-sql.mjs from ESPN's public NFL scoreboard API.
-- Season: ${season}
-- Games: ${games.length}

begin;

insert into public.season_weeks (season, week, week_sunday_date)
values
${seasonWeekValues}
on conflict (season, week) do update
set week_sunday_date = excluded.week_sunday_date;

insert into public.nfl_games (
  season,
  week,
  game_time,
  kickoff_at_utc,
  home_team,
  away_team,
  status,
  espn_event_id
)
values
${gameValues}
on conflict (espn_event_id) do update
set
  season = excluded.season,
  week = excluded.week,
  game_time = excluded.game_time,
  kickoff_at_utc = excluded.kickoff_at_utc,
  home_team = excluded.home_team,
  away_team = excluded.away_team,
  status = excluded.status;

commit;
`

await writeFile(outFile, sql)

console.log(`Wrote ${games.length} games and ${seasonWeeks.length} week rows to ${outFile}`)
